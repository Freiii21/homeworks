import React from 'react'
import Affair from './Affair'
import {AffairType, FilterType} from './HW2'
import s from './Affairs.module.css'

type AffairsPropsType = {
    data: Array<AffairType>
    setFilter: (filter: FilterType) => void
    deleteAffairCallback: (_id: number) => void
}

function Affairs(props: AffairsPropsType) {
    const mappedAffairs = props.data.map((a: AffairType) => (
        <Affair
            key={a._id}
            affair={a}
            deleteAffairCallback={props.deleteAffairCallback}
        />
    ))

    const setAll = () => {
        props.setFilter('all')
    }
    const setHigh = () => {
        props.setFilter('high')
    }
    const setMiddle = () => {
        props.setFilter('middle')
    }
    const setLow = () => {
        props.setFilter('low')
    }

    return (
        <div>

            {mappedAffairs}

            <div className={s.filterField}>
                <button onClick={setAll} className={s.filterAll}>All</button>
                <button onClick={setHigh} className={s.filterHigh}>High</button>
                <button onClick={setMiddle} className={s.filterMiddle}>Middle</button>
                <button onClick={setLow} className={s.filterLow}>Low</button>
            </div>
        </div>
    )
}

export default Affairs
